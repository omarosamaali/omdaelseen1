    public function index(Request $request)
    {
        $query = User::query();

        // Handle search
        if ($request->has('user_search') && $request->user_search) {
            $query->where('name', 'like', '%' . $request->user_search . '%');
        }

        // Handle filtering
        if ($request->has('filter')) {
            $filter = $request->filter;
            if ($filter === 'admin') {
                $query->where('role', 'admin');
            } elseif ($filter === 'user') {
                $query->where('role', 'user');
            } elseif ($filter === 'active') {
                $query->where('status', 1);
            } elseif ($filter === 'inactive') {
                $query->where('status', 0);
            } elseif ($filter === 'banned') {
                $query->where('status', 2);
            }
        }

        // Fetch users with places count and pagination
        $users = $query->withCount('places')->paginate(10);

        // Calculate counts
        $adminsCount = User::where('role', 'admin')->count();
        $usersCount = User::where('role', 'user')->count();
        $activeUsersCount = User::where('status', 1)->count();
        $inactiveUsersCount = User::where('status', 0)->count();
        $bannedUsersCount = User::where('status', 2)->count(); // Fixed inconsistency
        $deletedUsersCount = User::onlyTrashed()->count();

        $currentFilter = $request->filter;

        return view('admin.omdaHome.users.index', compact('users', 'adminsCount', 'usersCount', 'activeUsersCount', 'inactiveUsersCount', 'bannedUsersCount', 'deletedUsersCount', 'currentFilter'))
            ->with('layout', $this->layout);
    }

