    public function reportPlace(Request $request, $placeId)
    {
        try {
            $place = Places::findOrFail($placeId);

            $existingReport = Report::where('user_id', Auth::id())
                ->where('place_id', $placeId)
                ->first();

            if ($existingReport) {
                return response()->json([
                    'success' => false,
                    'error' => 'ููุฏ ุฃุจูุบุช ุนู ูุฐุง ุงูููุงู ูู ูุจู'
                ], 400);
            }

            $report = Report::create([
                'user_id' => Auth::id(),
                'place_id' => $placeId,
            ]);

            // ๐ข ุฅุดุนุงุฑ ูุตุงุญุจ ุงูููุงู
            $owner = $place->user;
            if ($owner && $owner->fcm_token) {
                $this->sendNotificationToOwner($owner, $place, $report);
            }

            // ๐ข ุฅุดุนุงุฑ ููู ุงูุฃุฏููุฒ
