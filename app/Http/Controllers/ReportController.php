                ->whereNotNull('fcm_token')
                ->pluck('fcm_token');

            foreach ($admins as $token) {
                $this->sendNotificationToAdmin($token, $place, $report);
            }

            return response()->json([
                'success' => true,
                'message' => 'ุชู ุชุณุฌูู ุงูุจูุงุบ ุจูุฌุงุญ'
